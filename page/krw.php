<p style="text-align: justify;">Organizacje Publicznej Służby Krwi Podstawą prawną działania Publicznej Służby Krwi (PSK) jest Ustawa o publicznej służbie krwi z dnia 22 sierpnia 1997 rok z późn. zm. Minister Zdrowia koordynuje działalność w zakresie organizacji pobierania krwi, oddzielania jej składników oraz zaopatrzenia w krew, a także zatwierdza plany działania w dziedzinie krwiodawstwa i krwiolecznictwa przygotowane przez Instytut. PSK w Polsce charakteryzuje jednolita struktura organizacyjna i jednolity system zapewnienia jakości. </p>
<p style="text-align: justify;">Do podstawowych zadań PSK należą: Zaopatrywanie podmiotów leczniczych w krew i jej składniki (kwalifikacja dawców, prowadzenie rejestru dawców, pobieranie, przetwarzanie, przechowywanie oraz wydawanie krwi i składników krwi), Propagowanie idei honorowego krwiodawstwa poprzez akcje promocyjne Prowadzenie działalności naukowo-badawczej w dziedzinie krwiodawstwa i krwiolecznictwa Podnoszenie kwalifikacji zawodowych, organizowanie szkoleń, itp. Udzielanie konsultacji związanych z leczeniem krwią i składnikami krwi. Nadzór nad podmiotami leczniczymi w zakresie czynności i badań związanych z przetaczaniem krwi. W ramach publicznej służby krwi w Polsce uprawnione do pobierania, przetwarzania krwi oraz zaopatrzenia podmiotów leczniczych w krew i składniki krwi są wyłącznie: 21 Regionalne Centra Krwiodawstwa i Krwiolecznictwa (RCKiK) wraz z podległymi oddziałami terenowymi</p>


